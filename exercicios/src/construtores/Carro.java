package construtores;

public class Carro {
	
	String marca;
	String modelo;
	int ano;
	int numPassageiro;
	
	public Carro(String marca, String modelo, int ano, int numPassageiro) {
		super();
		this.marca = marca;
		this.modelo = modelo;
		this.ano = ano;
		this.numPassageiro = numPassageiro;
	}
	
	
	

}
