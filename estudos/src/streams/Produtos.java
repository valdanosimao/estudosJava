package streams;

public class Produtos {
	
	String nome;
	double preco;
	double desconto;
	double valorFrete;
	
	public Produtos(String nome, double preco, double desconto, double valorFrete) {
		this.nome = nome;
		this.preco = preco;
		this.desconto = desconto;
		this.valorFrete = valorFrete;
	}
	
	

}
